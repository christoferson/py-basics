
# Equality ==
print(2 == 2)
print(2 == 1)
print('2' == 2)
print('Foo' == 'foo')

# NonEquality !=
print(2 != 2)
print(2 != 1)
print('2' != 2)
print('Foo' != 'foo')

# Greater Than > 
print(3 > 2)

# Greater Than or Equal To >= 
print(3 >= 3)

# Less Than < 
print(3 < 2)

# Less Than or Equal To <= 
print(3 <= 3)
